# assignment operators

# it helps to assign the value to a variable
# its add operator type of addition in value of previous score
a = 10
a+=2
print(a)

# its subtract operator type of subtraction in value of previous score
b = 20
b-= 5
print(b)

# its multiply operator type of multiplication in value of previous score
c = 5
c*=2
print(c)

# its divide operator type of division in value of previous score
d = 20
d/= 2
print(d)