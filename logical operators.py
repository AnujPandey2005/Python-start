# logical operators

# and operator
print(20>10 and 30<20)
# it will print false because in and operator both the conditions should be true

# or operator
print(20>10 or 30<20)
# it will print true because in or operator any one condition should be true

# not operator
print(not(20>10))
# it will print false because 20 is greater than 10 but we used not operator so it will print false 
